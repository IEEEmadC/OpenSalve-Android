package com.agzuniverse.agz.opensalve.Modals;

public class Contact {
    private String desc;
    private String number;

    public Contact(String desc, String number) {
        this.desc = desc;
        this.number = number;
    }

    public String getDesc() {
        return desc;
    }

    public String getNumber() {
        return number;
    }
}
