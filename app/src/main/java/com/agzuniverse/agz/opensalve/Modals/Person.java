package com.agzuniverse.agz.opensalve.Modals;

public class Person {
    private String name;
    private String bloodgrp;

    public Person(String name, String bloodgrp) {
        this.name = name;
        this.bloodgrp = bloodgrp;
    }

    public String getName() {
        return name;
    }

    public String getBloodgrp() {
        return bloodgrp;
    }
}
